#ifndef PLIBC_MACROS_H
#define PLIBC_MACROS_H

/*
 * Macros
 * with this lib you will be given simple generic macros
 * here is the list of all macros
 *         - UNUSED(x); use this macro to set it unused (for compile warnings)
 *         - TODO(str); exit program saying "[TODO] <file> -> str"
 *         - BIT(x); create a mask on bit x
 *         - KILOBYTE(x); convert x kilobyte to bytes
 *         - MEGABYTE(x); convert x megabyte to bytes
 *         - GIGABYTE(x); convert x gigabyte to bytes
 */

#include <stdlib.h>

#include "macros/macros.h"

#endif